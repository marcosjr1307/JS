<?
    $texto = " cotil - unicamp ";

    //trim - Retira o espaço no início e final de um string
    echo "-" . trim($texto) . "-" . "<br>";

    //Retira o espaço em branco (ou outros caracteres) do início da string
    echo "-" . ltrim($texto) . "-" . "<br>";

    //rtrim - Retira espaços em branco (ou outros caracteres) do fim da string
    echo "-" . rtrim($texto) . "-" . "<br>";
?>